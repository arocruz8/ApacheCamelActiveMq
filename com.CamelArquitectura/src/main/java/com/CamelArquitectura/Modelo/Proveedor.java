package com.CamelArquitectura.Modelo;

public class Proveedor {

	private String pNomProveedor;
	private String pFirma;
	private int pMontoCobro;
	
	public String getpNomProveedor() {
		return pNomProveedor;
	}
	public void setpNomProveedor(String pNomProveedor) {
		this.pNomProveedor = pNomProveedor;
	}
	public String getpFirma() {
		return pFirma;
	}
	public void setpFirma(String pFirma) {
		this.pFirma = pFirma;
	}
	public int getpMontoCobro() {
		return pMontoCobro;
	}
	public void setpMontoCobro(int pMontoCobro) {
		this.pMontoCobro = pMontoCobro;
	}
	
	
}
